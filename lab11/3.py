# WAP to demonstrate use of math module

import math
a = 10
b = 2
numlst = [10,20,23,30,40,50]
print("euler: ", math.e)
print("PI: ", math.pi)
print("Tau: ", math.tau)
print("infinity: ", math.inf)
print("NaN: ", math.nan)

print(" ")

print("ceil: ", math.ceil(a))
print("floor: ", math.floor(a))
print("factorial: ", math.factorial(a))
print("power: ", math.pow(a, b))
print("Absolute: ", math.fabs(a))
print("fmod: ", math.fmod(a, b))
print("fsum: ", math.fsum(numlst))
print("square root: ", math.sqrt(a))
print("truncate: ", math.trunc(a))


